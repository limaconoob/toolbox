int				env_parse(char *av)
{
	int			i;

	if (av[0] < 32 || av[0] > 122 || av[0] == '=')
		return (0);
	i = -1;
	while (av[++i])
		if (av[i] == '?' || av[i] == '$')
			return (-1);
	i = -1;
	while (av[++i])
		if (av[i] == '=')
			return (i);
	return (0);
}
