int					ft_isbrack2(char c)
{
	if (c == '\"' || c == '(' || c == ')' || c == '[' || c == ']')
		return (1);
	else if (c == '{' || c == '}' || c == '`' || c == '\'')
		return (1);
	else
		return (0);
}
