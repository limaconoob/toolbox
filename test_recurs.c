#include <stdio.h>

static void	ft_noob(int k)
{
	if (k < 10) {
		printf("1k:%d\n", k);
		ft_noob(++k);
		printf("2k:%d\n", k);
	}
}

int			main(void)
{
	static int k = 1;
	ft_noob(k);
	return 0;
}
