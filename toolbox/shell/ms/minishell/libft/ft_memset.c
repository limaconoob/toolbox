#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char *str;

	str = (char*)b;
	if (c < 0)
		c = -c;
	while (len > 0)
	{
		*str = c + '\0';
		str++;
		len--;
	}
	return (b);
}
