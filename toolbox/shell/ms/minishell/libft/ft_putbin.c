#include "libft.h"
#include <unistd.h>

void		ft_putbin(long k, long ult)
{
	if (k < 2147483648)
	{
		ft_putbin(k * 2, ult);
		if (ult & k)
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	if (ult == 1)
		write(1, "\n", 1);
}
