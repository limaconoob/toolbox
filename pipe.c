#include <unistd.h>
#include <fcntl.h>

int main(void)
{
  int fd[2];
  pipe(fd);
  dup2(fd[0], 0);
  dup2(fd[1], open("./test.txt", O_WRONLY));
  write(1, "bonjour\n", 8);
}
